public class table {
   public static void main(String[] args) {
    int n =10;
    System.out.println("print the table");
   for(int i=1;i<=10; i++){
    
      System.out.println(n*i);
   }

  System.out.println("print table in lower order ");
  for(int i=10; i>=1;i--){
    System.out.println(n*i);
  }
   } 
}


